package aima.core.util.datastructure;

public class GridLocation2D {

    private int x; //abcissae
    private int y; //ordinate

    public GridLocation2D(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
